

-------------------------------------------------------------------------------
--  notification catalog
-------------------------------------------------------------------------------
CREATE TABLE NOTIFICATION_CATALOG(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL,
    CODE VARCHAR(50),
    NAME VARCHAR(200),

    PRIORITY INT,
    CREATE_TIME TIMESTAMP,
    CREATOR VARCHAR(64),
    UPDATE_TIME TIMESTAMP,
    UPDATER VARCHAR(64),
    STATUS VARCHAR(50),

    CONSTRAINT PK_NOTIFICATION_CATALOG PRIMARY KEY(ID)
);

COMMENT ON TABLE NOTIFICATION_CATALOG IS '通知分类';
COMMENT ON COLUMN NOTIFICATION_CATALOG.ID IS 'id';
COMMENT ON COLUMN NOTIFICATION_CATALOG.CODE IS '编码';
COMMENT ON COLUMN NOTIFICATION_CATALOG.NAME IS '名称';
COMMENT ON COLUMN NOTIFICATION_CATALOG.PRIORITY IS '优先级';
COMMENT ON COLUMN NOTIFICATION_CATALOG.CREATE_TIME IS '创建时间';
COMMENT ON COLUMN NOTIFICATION_CATALOG.CREATOR IS '创建人';
COMMENT ON COLUMN NOTIFICATION_CATALOG.UPDATE_TIME IS '更新时间';
COMMENT ON COLUMN NOTIFICATION_CATALOG.UPDATER IS '更新人';
COMMENT ON COLUMN NOTIFICATION_CATALOG.STATUS IS '状态';

