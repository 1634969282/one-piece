

-------------------------------------------------------------------------------
--  notification provider
-------------------------------------------------------------------------------
CREATE TABLE NOTIFICATION_PROVIDER(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL,
    CODE VARCHAR(50),
    NAME VARCHAR(50),
    CONTENT VARCHAR(65535),
    USERNAME VARCHAR(50),
    PASSWORD VARCHAR(50),

    PRIORITY INT,
    CREATE_TIME TIMESTAMP,
    CREATOR VARCHAR(64),
    UPDATE_TIME TIMESTAMP,
    UPDATER VARCHAR(64),
    STATUS VARCHAR(50),

    CATALOG_ID BIGINT,

    CONSTRAINT PK_NOTIFICATION_PROVIDER PRIMARY KEY(ID),
    CONSTRAINT FK_NOTIFICATION_PROVIDER_CATALOG FOREIGN KEY (CATALOG_ID) REFERENCES NOTIFICATION_CATALOG(ID)
);

COMMENT ON TABLE NOTIFICATION_PROVIDER IS '通知供应商';
COMMENT ON COLUMN NOTIFICATION_PROVIDER.ID IS 'id';
COMMENT ON COLUMN NOTIFICATION_PROVIDER.CODE IS '编码';
COMMENT ON COLUMN NOTIFICATION_PROVIDER.NAME IS '名称';
COMMENT ON COLUMN NOTIFICATION_PROVIDER.CONTENT IS '内容';
COMMENT ON COLUMN NOTIFICATION_PROVIDER.USERNAME IS '账号';
COMMENT ON COLUMN NOTIFICATION_PROVIDER.PASSWORD IS '密码';
COMMENT ON COLUMN NOTIFICATION_PROVIDER.PRIORITY IS '优先级';
COMMENT ON COLUMN NOTIFICATION_PROVIDER.CREATE_TIME IS '创建时间';
COMMENT ON COLUMN NOTIFICATION_PROVIDER.CREATOR IS '创建人';
COMMENT ON COLUMN NOTIFICATION_PROVIDER.UPDATE_TIME IS '更新时间';
COMMENT ON COLUMN NOTIFICATION_PROVIDER.UPDATER IS '更新人';
COMMENT ON COLUMN NOTIFICATION_PROVIDER.STATUS IS '状态';
COMMENT ON COLUMN NOTIFICATION_PROVIDER.CATALOG_ID IS '分类';

