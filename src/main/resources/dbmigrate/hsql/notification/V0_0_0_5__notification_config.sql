

-------------------------------------------------------------------------------
--  notification config
-------------------------------------------------------------------------------
CREATE TABLE NOTIFICATION_CONFIG(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL,
    CODE VARCHAR(50),
    NAME VARCHAR(50),
    CONTENT VARCHAR(65535),

    PRIORITY INT,
    CREATE_TIME TIMESTAMP,
    CREATOR VARCHAR(64),
    UPDATE_TIME TIMESTAMP,
    UPDATER VARCHAR(64),
    STATUS VARCHAR(50),

    APP VARCHAR(50),

    CATALOG_ID BIGINT,

    CONSTRAINT PK_NOTIFICATION_CONFIG PRIMARY KEY(ID),
    CONSTRAINT FK_NOTIFICATION_CONFIG_CATALOG FOREIGN KEY (CATALOG_ID) REFERENCES NOTIFICATION_CATALOG(ID)
);

COMMENT ON TABLE NOTIFICATION_CONFIG IS '通知配置';
COMMENT ON COLUMN NOTIFICATION_CONFIG.ID IS 'id';
COMMENT ON COLUMN NOTIFICATION_CONFIG.CODE IS '编码';
COMMENT ON COLUMN NOTIFICATION_CONFIG.NAME IS '名称';
COMMENT ON COLUMN NOTIFICATION_CONFIG.CONTENT IS '内容';
COMMENT ON COLUMN NOTIFICATION_CONFIG.PRIORITY IS '优先级';
COMMENT ON COLUMN NOTIFICATION_CONFIG.CREATE_TIME IS '创建时间';
COMMENT ON COLUMN NOTIFICATION_CONFIG.CREATOR IS '创建人';
COMMENT ON COLUMN NOTIFICATION_CONFIG.UPDATE_TIME IS '更新时间';
COMMENT ON COLUMN NOTIFICATION_CONFIG.UPDATER IS '更新人';
COMMENT ON COLUMN NOTIFICATION_CONFIG.STATUS IS '状态';
COMMENT ON COLUMN NOTIFICATION_CONFIG.APP IS 'APP';
COMMENT ON COLUMN NOTIFICATION_CONFIG.CATALOG_ID IS '分类';

