

-------------------------------------------------------------------------------
--  notification log
-------------------------------------------------------------------------------
CREATE TABLE NOTIFICATION_LOG(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL,
    CODE VARCHAR(50),
    NAME VARCHAR(50),
    CONTENT VARCHAR(65535),
    CREATE_TIME TIMESTAMP,
    CREATOR VARCHAR(64),
    STATUS VARCHAR(50),
    APP VARCHAR(50),

    CATALOG VARCHAR(50),
    CONSTRAINT PK_NOTIFICATION_LOG PRIMARY KEY(ID)
);

COMMENT ON TABLE NOTIFICATION_LOG IS '通知日志';
COMMENT ON COLUMN NOTIFICATION_LOG.ID IS 'id';
COMMENT ON COLUMN NOTIFICATION_LOG.CODE IS '编码';
COMMENT ON COLUMN NOTIFICATION_LOG.NAME IS '名称';
COMMENT ON COLUMN NOTIFICATION_LOG.CONTENT IS '内容';
COMMENT ON COLUMN NOTIFICATION_LOG.CREATE_TIME IS '创建时间';
COMMENT ON COLUMN NOTIFICATION_LOG.CREATOR IS '创建人';
COMMENT ON COLUMN NOTIFICATION_LOG.STATUS IS '状态';
COMMENT ON COLUMN NOTIFICATION_LOG.APP IS 'APP';
COMMENT ON COLUMN NOTIFICATION_LOG.CATALOG IS '分类';

